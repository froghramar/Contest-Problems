const int N = 109;

char s[N][N];
int n, m, cnt[26], dx[] = {-1, 0, 1};

bool valid(int x, int y){
	return x > -1 and x < n and y > -1 and y < m;
}

bool valid2(int x, int y){
	return x > -1 and x < 7 and y > -1 and y < 5;
}

bool ok(int x, int y, int a, int b, int k, char ch){
	for(int i = 0; i < 3; i++){
		for(int j = 0; j < 3; j++){
			if(i == 1 and j == 1) continue;
			int xx = x + dx[i], yy = y + dx[j];
			if(valid(xx, yy) and s[xx][yy] == ch and (!valid2(a + dx[i], b + dx[j]) or p[k][a + dx[i]][b + dx[j]] == '0')) return 0;
		}
	}
	return 1;
}

void init(){
	int T;
	scanf("%d",&T);
	for(int t=1; t<=T; t++){
		scanf("%d%d",&n,&m);
		for(int i = 0; i < n; i++){
			scanf("%s",s[i]);
		}
		memset(cnt,0,sizeof(cnt));
		printf("Case %d:\n",t);
		for(int i = 0; i <= n - 7; i++){
			for(int j = 0; j <= m - 5; j++){
				for(int k = 0; k < 26; k++){
					set <char> A;
					bool res = 1;
					for(int a = 0; a < 7 and res; a++){
						for(int b = 0; b < 5; b++){
							if(p[k][a][b] == '0') continue;
							A.insert(s[i + a][j + b]);
							if(A.size() > 1){
								res = 0;
								break;
							}
							if(!ok(i + a, j + b, a, b, k, s[i + a][j + b])){
								res = 0;
								break;
							}
						}
					}
					if(!res) continue;
					cnt[k]++;
				}
			}
		}
		bool b = 0;
		for(int i = 0; i < 26; i++){
			if(cnt[i]){
				printf("%c %d\n",'A'+i,cnt[i]);
				b = 1;
			}
		}
		if(!b) puts("No Alphabet Found!");
	}
}

